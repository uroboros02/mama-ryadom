"""FastAPI-каркас Блока 0 (Шаг 0): пульс + приём вебхука.

На Шаге 0 это голые рельсы:
  · /health  — «бот жив» (пульс для мониторинга).
  · /webhook — принять вброшенный вебхук и быстро ответить 200.

Логики (проверка подписи, дедуп, роутер, память) тут ПОКА НЕТ — это Шаги 1–8.
Клиенту бот не пишет: Блок 0 — только трубы и предохранители.
"""

from fastapi import FastAPI, Request

app = FastAPI(title="Мама рядом — Блок 0 (каркас)")


@app.get("/health")
async def health():
    """Пульс: сервер поднят и отвечает."""
    return {"status": "ok"}


@app.post("/webhook")
async def webhook(request: Request):
    """Входящая дверь канала. На Шаге 0 — просто принимаем и подтверждаем (быстрый ack).

    Тело пока не разбираем: задача шага — доказать, что труба принимает вброс и отвечает 200.
    Разбор формата, проверка подписи и обработка фоном появятся на Шаге 1.
    """
    return {"received": True}
